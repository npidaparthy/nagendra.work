// ── Current language (read from localStorage or default en) ──────
function currentLang() {
  return localStorage.getItem('np-lang') || 'en';
}

// ── Apply a language to the whole page ───────────────────────────
function applyLanguage(lang) {
  if (!LANG[lang]) lang = 'en';
  localStorage.setItem('np-lang', lang);

  const strings = LANG[lang];

  // Update all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (strings[key] !== undefined) el.innerHTML = strings[key];
  });

  // Update dynamic CV content that has multilingual variants
  updateDynamicContent(lang);

  // Update search placeholder (data-i18n-placeholder elements)
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (strings[key] !== undefined) el.placeholder = strings[key];
  });

  // Update lang switcher active state
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Update <html lang> attribute
  const langAttr = { en: 'en', te: 'te', sa: 'sa' };
  document.documentElement.lang = langAttr[lang] || 'en';

  // Sync quote script picker to language default
  if (typeof window._quotesSetScript === 'function') {
    window._quotesSetScript(lang);
  }
}

// ── Update content rendered from cv-data.js ──────────────────────
function updateDynamicContent(lang) {
  const suffix = lang === 'en' ? '' : '_' + lang;

  // Hero tagline
  const taglineEl = document.querySelector('.js-tagline');
  if (taglineEl) {
    const val = CV.personal['tagline' + suffix] || CV.personal.tagline;
    taglineEl.textContent = val;
  }

  // Hero title
  const titleEl = document.querySelector('.js-title');
  if (titleEl) {
    const val = CV.personal['title' + suffix] || CV.personal.title;
    titleEl.textContent = val;
  }

  // About paragraphs
  const aboutEl = document.querySelector('.js-about-text');
  if (aboutEl) {
    const key = 'paragraphs' + suffix;
    const paras = CV.about[key] || CV.about.paragraphs;
    aboutEl.innerHTML = paras.map(p => `<p>${p}</p>`).join('');
  }

  // About stats labels
  document.querySelectorAll('.stat-label[data-stat-idx]').forEach(el => {
    const idx = +el.dataset.statIdx;
    const stat = CV.about.stats[idx];
    if (stat) el.textContent = stat['label' + suffix] || stat.label;
  });

  // Project titles + descriptions
  CV.projects?.forEach((proj, i) => {
    const titleEl = document.querySelector(`.js-proj-title-${i}`);
    if (titleEl) titleEl.textContent = proj['title' + suffix] || proj.title;
    const descEl = document.querySelector(`.js-proj-desc-${i}`);
    if (descEl) descEl.innerHTML = proj['desc' + suffix] || proj.desc_en;
  });

  // Gallery captions
  document.querySelectorAll('.gallery-caption[data-gallery-idx]').forEach(el => {
    const idx = +el.dataset.galleryIdx;
    const item = CV.gallery[idx];
    if (item) el.textContent = item['caption' + suffix] || item.caption;
  });

  // Sanskrit bio
  const saBioEl = document.querySelector('.js-sa-bio');
  if (saBioEl) {
    const val = CV.sanskrit['bio' + suffix] || CV.sanskrit.bio_en;
    saBioEl.innerHTML = val;
  }

  // Cert descriptions
  document.querySelectorAll('.js-cert-desc[data-cert-key]').forEach(el => {
    const [inst, idx] = el.dataset.certKey.split('|');
    const cert = CV.sanskrit.certifications.find(
      (c, i) => c.institution === inst && String(
        CV.sanskrit.certifications.filter(x => x.institution === inst).indexOf(c)
      ) === idx
    );
    if (cert) el.textContent = cert['desc' + suffix] || cert.desc_en || '';
  });

  // Texts studied tags
  document.querySelectorAll('.sa-text-tag[data-text-idx]').forEach(el => {
    const idx = +el.dataset.textIdx;
    const t = CV.sanskrit.texts[idx];
    if (t) el.textContent = (lang === 'en' ? t.en : lang === 'te' ? t.te : t.sa) || t.en;
  });

  // Sanskrit lipi names
  document.querySelectorAll('.lipi-name[data-lipi-idx]').forEach(el => {
    const idx = +el.dataset.lipiIdx;
    const lipi = CV.sanskrit.lipis[idx];
    if (lipi) el.textContent = lipi['name' + suffix] || lipi.name;
  });

  // Quote meanings
  document.querySelectorAll('.quote-meaning[data-quote-idx]').forEach(el => {
    const idx = +el.dataset.quoteIdx;
    const q = CV.quotes[idx];
    if (q) el.textContent = q['meaning' + suffix] || q.meaning_en;
  });

  // Reference book descriptions and authors
  CV.references?.forEach((b, i) => {
    const descEl = document.querySelector(`.js-ref-desc-${i}`);
    if (descEl) descEl.innerHTML = b['desc' + suffix] || b.desc_en;
    const authorEl = document.querySelector(`.js-ref-author-${i}`);
    if (authorEl) authorEl.textContent = b['author' + suffix] || b.author_en;
  });
}

// ── Build the language switcher UI ───────────────────────────────
function buildLangSwitcher() {
  const switcher = document.createElement('div');
  switcher.id = 'lang-switcher';
  switcher.innerHTML = `
    <button class="lang-btn" data-lang="en" title="English">EN</button>
    <button class="lang-btn" data-lang="te" title="తెలుగు">తె</button>
    <button class="lang-btn" data-lang="sa" title="संस्कृतम्">सं</button>
  `;

  // Insert before the Download CV button in nav
  const navInner = document.querySelector('.nav-inner');
  const btnNav   = navInner?.querySelector('.btn-nav');
  if (btnNav) navInner.insertBefore(switcher, btnNav);
  else navInner?.appendChild(switcher);

  switcher.querySelectorAll('.lang-btn').forEach(btn =>
    btn.addEventListener('click', () => applyLanguage(btn.dataset.lang))
  );
}

// ── Boot ─────────────────────────────────────────────────────────
document.addEventListener('DOMContentLoaded', () => {
  buildLangSwitcher();
  applyLanguage(currentLang());
});
